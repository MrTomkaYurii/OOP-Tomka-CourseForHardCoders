// Задача 4. Артеріальний тиск
//
// Напишіть програму, яка зчитує показники артеріального тиску
// і визначає його категорію.
//
// Категорії (за стандартом AHA):
//   систолічний < 120  І  діастолічний < 80   → норма
//   систолічний < 130  І  діастолічний < 80   → підвищений
//   систолічний < 140  АБО діастолічний < 90  → гіпертонія 1 ступеня
//   інакше                                     → гіпертонія 2 ступеня
//
// Вхідні дані:
//   систолічний тиск  — ціле число (мм рт. ст.)
//   діастолічний тиск — ціле число (мм рт. ст.)
//
// Вихідні дані:
//   Тиск: X/Y — Z
//
// Приклади:
//   Вхід    Вихід
//   115     Тиск: 115/75 — норма
//   75
//
//   Вхід    Вихід
//   125     Тиск: 125/78 — підвищений
//   78
//
//   Вхід    Вихід
//   135     Тиск: 135/88 — гіпертонія 1 ступеня
//   88
//
//   Вхід    Вихід
//   155     Тиск: 155/100 — гіпертонія 2 ступеня
//   100

public static class Task4
{
    public static void Run()
    {
        int systolic  = int.Parse(Console.ReadLine()!);
        int diastolic = int.Parse(Console.ReadLine()!);

        string status;
        if      (systolic < 120 && diastolic < 80) status = "норма";
        else if (systolic < 130 && diastolic < 80) status = "підвищений";
        else if (systolic < 140 || diastolic < 90) status = "гіпертонія 1 ступеня";
        else                                        status = "гіпертонія 2 ступеня";

        Console.WriteLine($"Тиск: {systolic}/{diastolic} — {status}");
    }
}
