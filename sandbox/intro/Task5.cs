// Задача 5. Розклад роботи клініки
//
// Напишіть програму, яка зчитує номер дня тижня
// і виводить назву дня та години роботи клініки.
//
// Розклад:
//   1 — Понеділок,  08:00–18:00
//   2 — Вівторок,   08:00–18:00
//   3 — Середа,     09:00–17:00
//   4 — Четвер,     08:00–18:00
//   5 — П'ятниця,   08:00–16:00
//   6 — Субота,     09:00–14:00
//   7 — Неділя,     вихідний
//
// Вхідні дані:
//   номер дня — ціле число від 1 до 7
//
// Вихідні дані:
//   День: <назва>, <години>
//   або
//   День: <назва> — вихідний
//
// Приклади:
//   Вхід   Вихід
//   1      День: Понеділок, 08:00–18:00
//
//   Вхід   Вихід
//   6      День: Субота, 09:00–14:00
//
//   Вхід   Вихід
//   7      День: Неділя — вихідний

public static class Task5
{
    public static void Run()
    {
        int day = int.Parse(Console.ReadLine()!);

        string info = day switch
        {
            1 => "Понеділок, 08:00–18:00",
            2 => "Вівторок, 08:00–18:00",
            3 => "Середа, 09:00–17:00",
            4 => "Четвер, 08:00–18:00",
            5 => "П'ятниця, 08:00–16:00",
            6 => "Субота, 09:00–14:00",
            7 => "Неділя — вихідний",
            _ => "невідомий день"
        };

        Console.WriteLine($"День: {info}");
    }
}
