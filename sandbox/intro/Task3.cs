// Задача 3. Вік та категорія пацієнта
//
// Напишіть програму, яка зчитує рік народження пацієнта,
// розраховує вік і визначає вікову категорію.
//
// Формула:
//   вік = 2026 - рік народження
//
// Категорії:
//   0–17  → дитина
//   18–59 → дорослий
//   60+   → пенсіонер
//
// Вхідні дані:
//   рік народження — ціле число (4 цифри)
//
// Вихідні дані:
//   Вік: X р.
//   Категорія: Y
//
// Приклади:
//   Вхід   Вихід
//   2010   Вік: 16 р.
//          Категорія: дитина
//
//   Вхід   Вихід
//   1990   Вік: 36 р.
//          Категорія: дорослий
//
//   Вхід   Вихід
//   1955   Вік: 71 р.
//          Категорія: пенсіонер

public static class Task3
{
    public static void Run()
    {
        int birthYear = int.Parse(Console.ReadLine()!);
        int age = 2026 - birthYear;

        string category;
        if      (age < 18) category = "дитина";
        else if (age < 60) category = "дорослий";
        else               category = "пенсіонер";

        Console.WriteLine($"Вік: {age} р.");
        Console.WriteLine($"Категорія: {category}");
    }
}
