// Задача 3. Тижневий графік клініки
//
// Зчитайте кількість пацієнтів за кожен день тижня (7 значень,
// починаючи з Понеділка). Виведіть таблицю, загальне число,
// найзавантаженіший і найтихіший день.
//
// Вхідні дані:
//   7 рядків   — пацієнтів за день (int): Пн, Вт, Ср, Чт, Пт, Сб, Нд
//
// Вихідні дані:
//   Таблиця: назва дня : кількість пацієнтів
//   Разом:        X
//   Найбільше:    День (X)
//   Найменше:     День (X)
//
// Приклади:
//   Вхід:       Вихід:
//   12          Понеділок   : 12 пацієнтів
//   8           Вівторок    : 8 пацієнтів
//   15          Середа      : 15 пацієнтів
//   10          Четвер      : 10 пацієнтів
//   9           П'ятниця    : 9 пацієнтів
//   5           Субота      : 5 пацієнтів
//   2           Неділя      : 2 пацієнтів
//               Разом:        61
//               Найбільше:    Середа (15)
//               Найменше:     Неділя (2)
//
//   Вхід:       Вихід:
//   5           Понеділок   : 5 пацієнтів
//   5           Вівторок    : 5 пацієнтів
//   5           Середа      : 5 пацієнтів
//   5           Четвер      : 5 пацієнтів
//   5           П'ятниця    : 5 пацієнтів
//   0           Субота      : 0 пацієнтів
//   0           Неділя      : 0 пацієнтів
//               Разом:        25
//               Найбільше:    Понеділок (5)
//               Найменше:     Субота (0)

public static class Task3
{
    public static void Run()
    {
        string[] days = { "Понеділок", "Вівторок", "Середа", "Четвер", "П'ятниця", "Субота", "Неділя" };
        int[] patients = new int[7];
        for (int i = 0; i < 7; i++)
            patients[i] = int.Parse(Console.ReadLine()!);

        int total = 0, maxIdx = 0, minIdx = 0;
        for (int i = 0; i < 7; i++)
        {
            total += patients[i];
            if (patients[i] > patients[maxIdx]) maxIdx = i;
            if (patients[i] < patients[minIdx]) minIdx = i;
        }

        for (int i = 0; i < 7; i++)
            Console.WriteLine($"{days[i],-12}: {patients[i]} пацієнтів");

        Console.WriteLine($"Разом:        {total}");
        Console.WriteLine($"Найбільше:    {days[maxIdx]} ({patients[maxIdx]})");
        Console.WriteLine($"Найменше:     {days[minIdx]} ({patients[minIdx]})");
    }
}
